print(d.strftime("%B"))
print(d.strftime("%b"))
print(d.strftime("%C"))
print(d.strftime("%c"))
print(d.strftime("%D"))
print(d.strftime("%d"))
print(d.strftime("%E"))
print(d.strftime("%e"))
print(d.strftime("%F"))