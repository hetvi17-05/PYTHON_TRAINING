#for loop: for loop is a sequence control loop
for i in range(5):  #by default it will start from 0 (in range if you are not giving
                    # starting point then by default it start from 0)
     print(i)

#(variance between number)descending  order loop :- in range(5,0,-1) [5: starting, o:ending, -1: descending]
for i in range(5,0,-1):
    print(i) 

#range takes 1 step ahead from 1
for i in range(1,11,2):
    print(i)

